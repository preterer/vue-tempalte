/**
 * Locale
 *
 * @export
 * @interface Locale
 */
export interface Locale {
  /**
   * Language code
   *
   * @type {string}
   * @memberof Locale
   */
  code: string;

  /**
   * Title to display
   *
   * @type {string}
   * @memberof Locale
   */
  title: string;

  /**
   * Path to svg file with flag
   *
   * @type {string}
   * @memberof Locale
   */
  svgPath: string;
}
